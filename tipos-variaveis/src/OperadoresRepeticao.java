public class OperadoresRepeticao {
    public static void main(String[] args) {
        int numero = 5;
        boolean variavel = true;

        // x repeticao
        // numero = numero++;
        variavel = !variavel;

        System.out.println(variavel);

        System.out.println(++numero);

        System.out.println(numero);

        System.out.println(--numero);

    }

}
