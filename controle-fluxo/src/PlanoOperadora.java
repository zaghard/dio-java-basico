public class PlanoOperadora {
    public static void main(String[] args) {
        String plano = "M"; // B /T;

        if (plano == "B") {
            System.out.println("100 minutos de ligação");


        } else if (plano == "M") {
            System.out.println("100 minutos de ligação");
            System.out.println("Whatsapp e Instagram Grátis");
            
        } else if (plano == "T") {
            System.out.println("100 minutos de ligação");
            System.out.println("Whatsapp e Instagram Grátis");
            System.out.println("5G Youtube");
        }
    }

}
